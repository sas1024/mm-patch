func ApplyPatch(fname string) error {
	br, err := os.ReadFile(fname)
	if err != nil {
		return err
	}

	idx := bytes.Index(br, pubkeyPattern)
	if idx == -1 {
		return fmt.Errorf("pattern not found in %s, patch not applied", fname)
	}

	fmt.Printf("Patching %s at offset=0x%02x\n", fname, idx)

	out := bytes.Replace(br, pubkeyPattern, replacePattern, -1)
	fmt.Println("Patch applied successfully! Now it's time to generate license :)")

	return os.WriteFile(fname, out, 0644)
}
